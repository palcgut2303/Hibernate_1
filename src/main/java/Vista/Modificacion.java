    private void iniciarComponentes() {
        iniciarCombo();
        jCombo.setSelectedIndex(0);
        jTTitulo.setText("");
        jTAutor.setText("");
        jTCategoria.setText("");
        jTPrecio.setText("");
    }

    private void iniciarCombo() {
        modelo = new DefaultComboBoxModel();
        List<Album> albumes = db.cargarAlbumes();
        for (Album album : albumes) {
            modelo.addElement(album.getId());
        }
        jCombo.setModel(modelo);
    }

    private void cargarDatosAlbum(){
        jCombo.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent arg0) {
                 int id = (Integer) jCombo.getSelectedItem();
                 Album album = db.getAlbum(id);
                 jTAutor.setText(album.getAutor());
                 jTCategoria.setText(album.getCategoria());
                 jTPrecio.setText(String.valueOf(album.getPrecio()));
                 jTTitulo.setText(album.getTitulo());
            }
        });
        
        
        
    }
    
