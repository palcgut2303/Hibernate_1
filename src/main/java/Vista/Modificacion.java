    private void jComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboItemStateChanged
        int numero = Integer.parseInt(jCombo.getSelectedItem().toString());
        albumSeleccionado = db.getAlbum(numero);
        jTTitulo.setText(albumSeleccionado.getTitulo());
        jTAutor.setText(albumSeleccionado.getAutor());
        jTCategoria.setText(albumSeleccionado.getCategoria());
        jTPrecio.setText(albumSeleccionado.getPrecio() + "");
    }//GEN-LAST:event_jComboItemStateChanged

    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed
        int id = (Integer) jCombo.getSelectedItem();
        albumSeleccionado = db.getAlbum(id);
        db.Borrar(albumSeleccionado);
        iniciarComponentes();
        JOptionPane.showMessageDialog(this, "Borrado correctamente", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jBBorrarActionPerformed

    private void jBActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBActualizarActionPerformed
        Album a = new Album(Integer.parseInt(jCombo.getSelectedItem().toString()), jTTitulo.getText().toString(), jTAutor.getText().toString(), Integer.parseInt(jTPrecio.getText().toString()), jTCategoria.getText().toString());
        db.Actualizar(a);
       
        iniciarComponentes();
        JOptionPane.showMessageDialog(this, "Actualizado correctamente", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jBActualizarActionPerformed

